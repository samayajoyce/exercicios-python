'''Desenvolva um programa que leia as notas de um aluno calcule e mostre a sua media'''
#esse programa é iniciante entao vou usar duas notas e montar de modo mais simples.
nota1 = float(input('Digite a primeira nota: '))
nota2 = float(input('Digite a segunda nota: '))
# calculando a media
media = (nota1+nota2)/2
#mostrando o resultado do calculo
print(f'A sua media é {media}')